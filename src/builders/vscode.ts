export const buildVsCodeColors = (theme: ThemeSpec): Record<string, string> =>
  buildVsCodeColorsFromModel(deriveThemeModel(theme));

export const buildThemeFromModel = (model: ResolvedThemeModel): ThemeDefinition => ({
  $schema: "vscode://schemas/color-theme",
  name: model.theme.displayName,
  type: model.theme.type,
  semanticHighlighting: true,
  colors: buildVsCodeColorsFromModel(model),
  tokenColors: buildTokenColorsFromModel(model),
  semanticTokenColors: buildSemanticTokenColorsFromModel(model)
});

export const buildTheme = (theme: ThemeSpec): ThemeDefinition => buildThemeFromModel(deriveThemeModel(theme));
