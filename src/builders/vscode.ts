const baseSemanticTokens = (theme: ThemeSpec) => {
  const syntax = theme.base16;
  return {
    typeParameter: syntax.base0A,
    class: syntax.base0E,
    interface: syntax.base0E,
    enum: syntax.base0A,
    enumMember: syntax.base09,
    struct: syntax.base0E,
    decorator: syntax.base0D,
    macro: syntax.base08,
    label: syntax.base0A,
    event: syntax.base08,
    modifier: syntax.base08,
    operator: syntax.base05,
    "variable.defaultLibrary": syntax.base09,
    "variable.readonly.defaultLibrary": syntax.base09,
    "type.defaultLibrary": syntax.base0A,
    "variable.readonly": syntax.base05,
    selfKeyword: syntax.base0E
  };
};

export const buildTokenColorsFromModel = (model: ResolvedThemeModel) =>
  toVsCodeTokenColors(model.syntax);

export const buildSemanticTokenColorsFromModel = (model: ResolvedThemeModel) => ({
  ...toSemanticTokens(model.syntax),
  ...baseSemanticTokens(model.theme)
});

export const buildTokenColors = (theme: ThemeSpec) => buildTokenColorsFromModel(deriveThemeModel(theme));

export const buildSemanticTokenColors = (theme: ThemeSpec) =>
  buildSemanticTokenColorsFromModel(deriveThemeModel(theme));

